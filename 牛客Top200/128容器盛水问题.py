"""
给定一个整形数组arr，已知其中所有的值都是非负的，将这个数组看作一个容器，请返回容器能装多少水。
"""


# max water
# @param arr int整型一维数组 the array
# @return long长整型
#
class Solution:
    def maxWater(self, arr):
        if len(arr) < 3:
            return 0
        # 返回值
        res = 0
        low = 0
        temp = 0
        for i in range(len(arr)):
            if arr[low] > arr[i]:
                temp = temp + arr[low] - arr[i]
            if arr[low] <= arr[i]:
                res = res + temp
                temp = 0
                low = i
        low = len(arr) - 1
        temp = 0
        for j in range(len(arr) - 1, 0, -1):
            if arr[low] > arr[j]:
                temp = temp + arr[low] - arr[j]
            if arr[low] < arr[j]:
                res = res + temp
                temp = 0
                low = j
        return res


